            // Function to close menu with GSAP Timeline animation (reverse)
            function closeMenu() {
                if (!isMenuOpen || !menuTimeline) return;

                // Kill existing timeline if still running
                if (menuTimeline) {
                    menuTimeline.kill();
                }

                // Create reverse timeline for closing - Faster animation
                const closeTimeline = gsap.timeline({
                    defaults: {
                        ease: "power2.in"
                    },
                    onComplete: function() {
                        hoverHeaderContainer.classList.remove('active');
                        body.classList.remove('menu-open');
                        body.style.overflow = '';
                        if (mobileMenuToggle) {
                            mobileMenuToggle.classList.remove('active');
