                closeTimeline.to([...headerNavItemsSecondary, ...headerNavItemsMain], {
                    x: navItemsX,
                    opacity: 0,
                    duration: 0.3,
                    stagger: 0.02,
                    ease: "power2.in"
                }, "-=0.1");

                // Container (bg + content) right ko move karte hue chala jaye
