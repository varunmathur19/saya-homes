        function initMenuAnimation() {
            if (typeof gsap === 'undefined') {
                console.warn('GSAP is not loaded. Falling back to basic toggle.');
                // Fallback to basic toggle if GSAP is not available
                initBasicMenuToggle();
                return;
            }

            const mobileMenuToggle = document.getElementById('mobileMenuToggle');
            const hoverHeaderContainer = document.querySelector('.hover-header-container');
            const hoverHeaderContent = document.querySelector('.hover-header-content');
            const hoverHeaderContentLeft = document.querySelector('.hover-header-content-left');
            const hoverHeaderContentRight = document.querySelector('.hover-header-content-right');
            const headerSocialLinks = document.querySelector('.header-social-links');
