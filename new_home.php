            const headerSocialLinkLeft = document.querySelector('.header-social-link-left');
            const hoverHeaderClose = document.getElementById('hoverHeaderClose');
            const body = document.body;

            let menuTimeline = null;
            let isMenuOpen = false;

            // Initialize element positions for animation - Text content and social icons
            const isMobile = window.innerWidth < 768;
            const isTablet = window.innerWidth >= 768 && window.innerWidth < 992;

            // Container (bg + content): right se aayega – start off-screen right
            gsap.set(hoverHeaderContainer, {
                x: '100%',
                opacity: 0,
                visibility: 'hidden'
            });

            // Content container ke andar hai, isliye saath move karega; position ready
            gsap.set(hoverHeaderContent, {
                x: 0,
                opacity: 1
            });

            // Initialize text navigation items
            gsap.set([...headerNavItemsMain, ...headerNavItemsSecondary], {
                x: isMobile ? 30 : isTablet ? 40 : 50,
                opacity: 0
            });

            // Initialize social media icons
            gsap.set(headerSocialIcons, {
                y: 30,
                opacity: 0,
                scale: 0.8
            });

            // Initialize social link left (call section) - opacity nahi, sirf position
            if (headerSocialLinkLeft) {
                gsap.set(headerSocialLinkLeft, { x: -30 });
            }

            // Function to open menu with GSAP Timeline animation
            function openMenu() {
                if (isMenuOpen || menuTimeline) return;

                isMenuOpen = true;
                hoverHeaderContainer.classList.add('active');
                body.classList.add('menu-open');
                body.style.overflow = 'hidden';

                // Create GSAP Timeline for opening animation - Faster and more attractive
                menuTimeline = gsap.timeline({
                    defaults: {
                        ease: "power2.out"
                    }
                });

                // Background image + content: right se move karte hue aate hue (2s)
                gsap.set(hoverHeaderContainer, { visibility: 'visible' });
                menuTimeline.to(hoverHeaderContainer, {
                    x: 0,
                    opacity: 1,
                    duration: 2,
                    ease: "power2.out"
                });

                // Content ke andar hai, saath mein aata hai; nav/social stagger se "aate hue" dikhe
                const navItemsX = window.innerWidth >= 768 ? 50 : window.innerWidth >= 480 ? 30 : 20;
                menuTimeline.to(headerNavItemsMain, {
                    x: 0,
                    opacity: 1,
                    duration: 0.45,
                    stagger: 0.05,
                    ease: "power2.out"
                }, "-=1.4");

                menuTimeline.to(headerNavItemsSecondary, {
                    x: 0,
                    opacity: 1,
                    duration: 0.4,
                    stagger: 0.04,
                    ease: "power2.out"
                }, "-=0.3");

                if (headerSocialLinkLeft) {
                    menuTimeline.to(headerSocialLinkLeft, {
                        x: 0,
                        duration: 0.5,
                        ease: "back.out(1.2)"
