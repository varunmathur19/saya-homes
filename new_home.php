            // Hamburger menu toggle
            if (mobileMenuToggle && hoverHeaderContainer) {
                mobileMenuToggle.addEventListener('click', function(e) {
                    e.preventDefault();
                    e.stopPropagation();

                    if (isMenuOpen) {
