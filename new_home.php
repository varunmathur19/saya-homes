                    opacity: 0,
                    duration: 0.35,
                    stagger: 0.02,
                    ease: "power2.in"
                }, "-=0.15");

                closeTimeline.to(hoverProjectHeaderContent, {
                    y: '-100%',
                    opacity: 0,
                    duration: 0.7,
                    ease: "power3.in"
                }, "-=0.3");

                closeTimeline.to(hoverProjectHeaderContainer, {
                    opacity: 0,
                    visibility: 'hidden',
                    duration: 0.2
                }, "-=0.5");
